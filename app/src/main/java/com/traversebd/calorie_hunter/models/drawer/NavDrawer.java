package com.traversebd.calorie_hunter.models.drawer;

public class NavDrawer {

    public int icon;
    public String title;

    public NavDrawer(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
